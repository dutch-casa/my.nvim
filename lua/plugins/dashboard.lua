return {
  {
    "folke/snacks.nvim",
    opts = {
      dashboard = {
        preset = {
          header = (function()
            local lines = {
              [[_________/\\\_____________________________________________/\\\_________        ]],
              [[ ________\/\\\____________________________________________\/\\\_________       ]],
              [[  ________\/\\\____________________/\\\____________________\/\\\_________      ]],
              [[   ________\/\\\___/\\\____/\\\__/\\\\\\\\\\\_____/\\\\\\\\_\/\\\_________     ]],
              [[    ___/\\\\\\\\\__\/\\\___\/\\\_\////\\\////____/\\\//////__\/\\\\\\\\\\__    ]],
              [[     __/\\\////\\\__\/\\\___\/\\\____\/\\\_______/\\\_________\/\\\/////\\\_   ]],
              [[      _\/\\\__\/\\\__\/\\\___\/\\\____\/\\\_/\\__\//\\\________\/\\\___\/\\\_  ]],
              [[       _\//\\\\\\\/\\_\//\\\\\\\\\_____\//\\\\\____\///\\\\\\\\_\/\\\___\/\\\_ ]],
              [[        __\///////\//___\/////////_______\/////_______\////////__\///____\///__]],
              [[                                                                               ]],
              [[                                                                               ]],
            }
            return table.concat(lines, "\n")
          end)(),
        },
      },
    },
    init = function()
      vim.api.nvim_create_autocmd("ColorScheme", {
        pattern = "*",
        callback = function()
          vim.api.nvim_set_hl(0, "SnacksDashboardHeader", { fg = "#ff9e64" })
        end,
      })
      vim.api.nvim_set_hl(0, "SnacksDashboardHeader", { fg = "#ff9e64" })
    end,
  },
}
